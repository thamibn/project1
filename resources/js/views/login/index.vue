<template>
  <div class="login-container">
    <ValidationObserver ref="loginForm" v-slot="{ handleSubmit }">
    <el-form v-on:keyup.enter.native="handleSubmit(handleLogin)" :model="loginForm" class="login-form" auto-complete="on" label-position="left">
      <h3 class="title">
        {{ $t('login.title') }}
      </h3>

      <ValidationProvider name="email" rules="required|email" v-slot="{ errors }">
        <el-form-item prop="email" :error="errors[0]">
          <span class="svg-container">
            <svg-icon icon-class="user" />
          </span>
          <el-input
            v-model="loginForm.email"
            name="email"
            type="test"
            auto-complete="on"
            placeholder="Email" />
        </el-form-item>
      </ValidationProvider>

      <ValidationProvider name="password" rules="required|min:6" v-slot="{ errors }">
        <el-form-item prop="password" :error="errors[0]">
          <span class="svg-container">
            <svg-icon icon-class="password" />
          </span>
          <el-input
            v-model="loginForm.password"
            :type="pwdType"
            name="password"
            auto-complete="on"
            placeholder="Password"
          />
          <span class="show-pwd" @click="showPwd">
            <svg-icon :icon-class="iconName" />
          </span>
        </el-form-item>
      </ValidationProvider>
      <el-form-item>
        <el-button :loading="loading" type="primary" style="width:100%;" @click.native.prevent="handleSubmit(handleLogin)">
          Sign in
        </el-button>
      </el-form-item>
      <div class="tips">
        <span style="margin-right:20px;">Email: admin@project.dev</span>
        <span>Password: 123456</span>
      </div>
    </el-form>
    </ValidationObserver>
  </div>
</template>

<script>
  export default {
  name: 'Login',
  data() {
    return {
      loginForm: {
        email: 'admin@project.dev',
        password: '123456',
      },
      loading: false,
      pwdType: 'password',
      iconName: "eye-close",
      redirect: undefined,
    };
  },
  watch: {
    $route: {
      handler: function(route) {
        this.redirect = route.query && route.query.redirect;
      },
      immediate: true,
    },
  },
  methods: {
    showPwd() {
      if (this.pwdType === 'password') {
        this.pwdType = 'text';
        this.iconName = "eye-open"
      } else {
        this.pwdType = 'password';
        this.iconName = "eye-close"
      }
    },
    handleLogin() {
    this.loading = true;
    this.$store.dispatch('user/login', this.loginForm)
      .then(() => {
        this.$router.push({ path: this.redirect || '/' });
        this.loading = false;
      })
      .catch(error => {
        this.$refs.loginForm.setErrors(error.response.data.error.message);
        this.loading = false;
      });
    },
  },
};
</script>

